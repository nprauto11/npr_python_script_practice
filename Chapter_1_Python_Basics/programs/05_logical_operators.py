# Logical Operators Example

x = 7
y = 3

# Using and, or, and not with conditions
print("x =", x, "y =", y)
print("x > 5 and y < 5:", x > 5 and y < 5)   # Both conditions are true
print("x > 5 or y > 5:", x > 5 or y > 5)     # One of the conditions is true
print("not(x < 5):", not(x < 5))             # Inverts the condition
