# example for Conditionals (If Statements)

# check_even_odd.py
# This program checks if a given number is even or odd using an if-else statement.

# Define the number to check
number = 7

# Check if the number is even
if number % 2 == 0:
    print(number, "is even.")
else:
    print(number, "is odd.")

# Explanation:
# - The script defines a number (`number`).
# - It uses an `if` statement to check if the number is divisible by 2.
# - If it is, it prints that the number is even; otherwise, it prints that the number is odd.
