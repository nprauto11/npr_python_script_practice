# count_to_five.py
# This program demonstrates the use of a while loop to print numbers from 1 to 5.

# Initialize a counter
count = 1

# Continue looping until the counter is greater than 5
while count <= 5:
    print("Current count is:", count)  # Print the current count
    count += 1  # Increment the counter by 1

# Explanation:
# - The script initializes a counter (`count`) starting at 1.
# - The while loop continues as long as count is less than or equal to 5.
# - In each iteration, it prints the current value of count and increments it by 1.
