# example for Conditionals (If Else Elif Statements)

# assign_grade.py
# This program assigns a grade based on a given score using an if-elif-else statement.

# Define the score
score = 85

# Check the score range and assign a grade
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
elif score >= 60:
    print("Grade: D")
else:
    print("Grade: F")

# Explanation:
# - The script defines a score (`score`).
# - It uses an `if-elif-else` structure to determine which grade corresponds to the score.
# - Depending on the score range, it prints the appropriate grade.
