# print_square_numbers.py
# This program demonstrates the use of a for loop to print the first 5 square numbers.

# Define the range for squares
for i in range(5):
    square = i ** 2  # Calculate the square of the current number
    print("The square of", i, "is", square)

# Explanation:
# - The script uses a for loop that iterates from 0 to 4 (5 is not included).
# - In each iteration, it calculates the square of the current number (i).
# - It then prints the current number and its square.
